        public static string LookupVendor(string macAddress)
        {
            if (string.IsNullOrEmpty(macAddress))
                return "未知设备";

            var normalizedMac = NormalizeMacAddress(macAddress);
            if (normalizedMac.Length < 8)
                return "未知设备";

            var oui = normalizedMac.Substring(0, 8);
            
            if (VendorDictionary.TryGetValue(oui, out var vendor))
            {
                return vendor;
            }

            return "未知设备";
        }

        private static string NormalizeMacAddress(string mac)
        {
            var normalized = mac.ToUpper().Replace("-", ":");
            var parts = normalized.Split(':');
            if (parts.Length >= 3)
            {
                return $"{PadHex(parts[0])}:{PadHex(parts[1])}:{PadHex(parts[2])}";
            }
            return normalized;
        }

        private static string PadHex(string hex)
        {
            if (hex.Length == 1)
                return "0" + hex;
            return hex;
        }

        public static bool IsLikelyMobileDevice(string vendor)
        {
            var mobileVendors = new[] { "Apple", "Samsung", "Huawei", "Xiaomi", "OPPO", "vivo", 
                "OnePlus", "Realme", "Motorola", "LG", "Sony", "HTC", "Lenovo", "ZTE", 
                "Google", "Nokia", "Asus", "Acer", "BlackBerry", "Meizu", "Honor", "Redmi" };
            
            foreach (var mv in mobileVendors)
            {
                if (vendor.Contains(mv, StringComparison.OrdinalIgnoreCase))
                    return true;
            }
            return false;
        }
